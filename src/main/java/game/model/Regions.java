package game.model;

public enum Regions {
    KANTO(1, "Kanto"),
    ;

    private int id;
    private String nom;

    Regions(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
